var canCompleteCircuit = function(gas, cost) {
    let total = 0;
    let tank = 0;
    let start = 0;

    for (let i = 0; i < gas.length; i++) {
        let diff = gas[i] - cost[i];
        total += diff;
        tank += diff;

        if (tank < 0) {
            start = i + 1;   
            tank = 0;        
        }
    }

    return total >= 0 ? start : -1;
};


let gas = [1,2,3,4,5], cost = [3,4,5,1,2];
console.log(canCompleteCircuit(gas, cost)); // 3
